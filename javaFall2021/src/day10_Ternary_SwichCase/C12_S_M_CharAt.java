package day10_Ternary_SwichCase;

public class C12_S_M_CharAt {

	public static void main(String[] args) {
		// charAt(istenilen index)  method u istedigimiz index deki 
		// harfi verir

		String kurs = "Techproeducation";
		System.out.println(kurs.charAt(5)); // 5. index 'deki karakteri verir
		
		// index 0'dan baslar
		System.out.println(kurs.charAt(0)); // ---> ilk harfi verir
		
		
		
	}

}
